/**
 * Variable 선언하기
 * 
 * 1) var - 더 이상 쓰지 않는다.
 * 2) let
 * 3) const
 */

var name = 'kkk123';
console.log(name);

var age = 30;
console.log(age);

let newJeans = '뉴진스';
console.log(newJeans);

/**
 * let과 var로 선언하면
 * 값을 추후 변경할 수 있다.
 */

newJeans = '강해린';
console.log(newJeans);

const ive = '아이브';
console.log(ive);

// ive = 'kkk123';